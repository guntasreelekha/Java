public class DataType
{
	  public static void main(String[] args)
	  {
		    int Num = 5;               
		    float FloatNum = 5.99f;   
		    char Letter = 'D';        
		    boolean Bool = true;       
		    String Text = "Hello";       
		    System.out.println(Num);
		    System.out.println(FloatNum);
		    System.out.println(Letter);
		    System.out.println(Bool);
		    System.out.println(Text);
	  }

}
